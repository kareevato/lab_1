import math

# Визначення значень x і y
x = 2.14
y = 0.578

# Обчислення виразу
term1 = math.sin(x ** 2)
term2 = math.sin(x ** 3) ** 2
term3 = math.sin(y ** 4) ** 3

# Підсумовування всіх частин
result = term1 + term2 + term3

# Виведення результату
print("Результат:", result)